package day0120.operator;

public class OperatorEx2 {
   public static void main(String[] args) {
		int v1=5;
	    int v2=2;
	    System.out.println("v1+v2= "+(v1+v2));
	    System.out.println("v1-v2= "+(v1-v2));
	    System.out.println("v1*v2= "+v1*v2);
	    System.out.println("v1/v2= "+v1/v2);
	    //int 와 int 나눗셈은 몫만 남는다.
	    System.out.println("v1%v2= "+v1%v2);
	    System.out.println("v1/v2= "+(double) v1/v2);
	    
	}

}
